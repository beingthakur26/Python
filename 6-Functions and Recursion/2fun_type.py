# Built-in function ;  
# Built-in Functions
# These are functions that Python provides by default.
# Examples:
# print()
# len()
# type()
# input()
# sum()

print(len("Hello"))  # Output: 5


# User Defined function ;
# Functions that are created by the user to perform specific tasks.

def greet(name):
    return f"Hello, {name}!"

print(greet("Alice"))  # Output: Hello, Alice!