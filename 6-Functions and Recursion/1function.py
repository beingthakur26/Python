# Function : 
# A function is a block of reusable code that performs a specific task.
# Functions help organize code, make it more readable, and allow reusability.


# Sum of a and b
# Function definition
def cal_sum(a, b) : # parameters
    sum = a + b 
    print(sum)
    return sum 

cal_sum(2, 4) # output : 6


# Multiplication of a and b
def cal_mult(a, b):
    return  a * b
mult = cal_mult(3, 4) # 12  # Function call; parameters
print(mult) # output : 12
