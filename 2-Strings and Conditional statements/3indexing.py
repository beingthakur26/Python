# Indexing
# Indexing is a way to access the characters in a string. It is done by using square brackets [] and the index of the character you want to access. The index of the first character is 0, the second character is 1, and so on. The index of the last character is -1, the second last character is -2, and so on.

str = "Manas singh"
# ch = str[5]
# print(ch)
print(str[0]) #M
print(str[1]) #a
print(str[5]) # (space)
print(str[8]) #n