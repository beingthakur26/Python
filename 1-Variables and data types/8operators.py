# Arithmetic operators
# +, -, *, /, %, **
a = 10
b =2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b) # remainder deta hai
print(a ** b) # power deta hai [a^b]


# Comparison operators / Relational operators
# ==, !=, >, <, >=, <=
a =50
b =20
print(a == b) # False
print(a != b) # True
print(a > b) # True
print(a < b) # False
print(a >= b) # True
print(a <= b) # False


# Assignment operators
# =, +=, -=, *=, /=, %=, **=
num = 10
num += 5
print( "num : ",num) # 15


# Logical operators
# and, or, not
print (not True)
print(not False)
